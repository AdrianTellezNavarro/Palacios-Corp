    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
    String proceso = "Por Latinoamericano";
    
Connection conexion = null;
try {
    String url = "jdbc:mysql://localhost:3306/relacional";
    String usuario = "root";
    String contraseñaDB = "Admin!01";
    
    conexion = DriverManager.getConnection(url, usuario, contraseñaDB);
    
    String sql = "UPDATE usuario SET Método_Actual = ? WHERE ID_Usuario = ?";
    PreparedStatement pst = conexion.prepareStatement(sql);
    pst.setString(1, proceso);
    pst.setInt(2, id);
    
    int rowsAffected = pst.executeUpdate();
    
    if (rowsAffected > 0) {
        System.out.println("Método actualizado correctamente");
    } else {
        System.out.println("No se encontró el usuario con ID: " + id);
    }
    
    pst.close();
    
    Opc_Proceso obj = new Opc_Proceso(proceso, contraseña, nombre, id, conexion);
    obj.setVisible(true);
    this.setVisible(false);
    
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(null, "Error de base de datos: " + ex.getMessage());
}
    }//GEN-LAST:event_jLabel5MouseClicked

