    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    try {
        String nombreMetodo = "Por Latinoamericano";
        
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/relacional", "root", "Admin!01");
        
        String sql = "SELECT Requisitos FROM metodo_naturalizacion WHERE Nombre_Metodo = ?";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, nombreMetodo);
        
        ResultSet rs = pstmt.executeQuery();
        
        if (rs.next()) {
            String requisitos = rs.getString("Requisitos");
            
            JOptionPane.showMessageDialog(this, "Requerimientos:\n" + requisitos.replace(",", "\n"), 
                                        "Requerimientos", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron requisitos para este método.", 
                                        "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        rs.close();
        pstmt.close();
        conn.close();
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error al consultar la base de datos: " + ex.getMessage(), 
                                    "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton6ActionPerformed

